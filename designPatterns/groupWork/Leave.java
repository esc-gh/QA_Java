package designPatterns.groupWork;

public class Leave {
	int numberOfDays;

	public Leave(int numberOfDays) {
		super();
		this.numberOfDays = numberOfDays;
	}

	public int getNumberOfDays() {
		return numberOfDays;
	}

	public void setNumberOfDays(int numberOfDays) {
		this.numberOfDays = numberOfDays;
	}

}
